#Python program to do arithmetical operations
print("calculator program >>>")
n1=int(input('enter first number:-'))
n2=int(input('enter second number:-'))

#add of two numbers
add=n1+n2
print('add of {0} and {1} is {2} '.format(n1 , n2 , add ))
#subtract of two number
sub=n1-n2
print('sub of {0} and {1} is {2} '.format(n1 , n2 , sub ))
mul=n1*n2
print('mul of {0} and {1} is {2} '.format(n1 , n2 , mul ))
div=n1/n2
print('div of {0} and {1} is {2} '.format(n1 , n2 , div ))
